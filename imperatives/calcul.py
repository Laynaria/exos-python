def add(a: float, b: float) -> float:
    """
    Function for an addition of two numbers
    
    :param a: First Number 
    :type a: float
    :param b: Second Number
    :type b: float
    :return: a + b
    :rtype: float
    """
    return a + b

def substract(a: float, b: float) -> float:
    """
    Function for substracting a number from another
    
    :param a: Number to be substracted from
    :type a: float
    :param b: Number which will be substracted
    :type b: float
    :return: a - b
    :rtype: float
    """
    return a - b

def multiply(a: float, b: float) -> float:
    """
    Function for multiplying two numbers
    
    :param a: Number a
    :type a: float
    :param b: Number b
    :type b: float
    :return: a * b
    :rtype: float
    """
    return a * b

def divide(a: float, b: float) -> float:
    """
    Fuction for dividing a number by another
    
    :param a: Number to be divided
    :type a: float
    :param b: Number which divides
    :type b: float
    :return: a / b
    :rtype: float
    """
    return a / b